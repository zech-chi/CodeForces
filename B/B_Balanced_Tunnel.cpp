#define abs(a) (((a) > 0) ? (a) : (-(a)))
#define max(a, b) (((a) > (b)) ? (a) : (b))
#define min(a, b) (((a) > (b)) ? (b) : (a))


class Solution {
private:

public:
    void solve() {
        int n; cin >> n;
        vector<int> a(n), b(n);

        for (int i = 0; i < n; i++) cin >> a[i];
        for (int i = 0; i < n; i++) cin >> b[i];

        int i = 0, j = 0;
        set<int> seen;

        while (i < n && j < n) {
            if (a[i] == b[j]) {
                i++;
                j++;
            } else if (seen.find(a[i]) != seen.end()) {
                i++;
            } else {
                seen.insert(b[j]);
                j++;
            }
        }

        cout << seen.size() << "\n";
    }

};


int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    Solution().solve();
    return (0);
}
