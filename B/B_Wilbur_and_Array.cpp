typedef long long ll;
using namespace std;
#define abs(a) (((a) > 0) ? (a) : (-(a)))
#define max(a, b) (((a) > (b)) ? (a) : (b))
#define min(a, b) (((a) > (b)) ? (b) : (a))


class Solution {
private:

public:
    void solve() {
		int n; cin >> n;
		int b, cur = 0;
		ll steps = 0;

		for (int i = 0; i < n; i++) {
			cin >> b;
			if (cur > b) {
				steps += cur - b;
				cur = b;
			} else if (cur < b) {
				steps += b - cur;
				cur = b;
			} else {
				continue;
			}
		}
		cout << steps << "\n";

    }

};


int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    Solution().solve();
    return (0);
}
