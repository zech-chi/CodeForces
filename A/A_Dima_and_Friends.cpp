void solve()
{
    int n; cin >> n;
    int finger;
    int sum = 0;
    for (int i = 0; i < n; i++) {
        cin >> finger;
        sum += finger;
    }

    int choices = 0;
    for (int choice = 1; choice <= 5; choice++) {
        if ((sum + choice) % (n + 1) != 1)
            choices++;
    }

    cout << choices << "\n";
}

