class Solution {
private:

public:
    int gcd(int a, int b) {
        while (b != 0) {
            int tmp = b;
            b = a % b;
            a = tmp;
        }
        return a;
    }

    int lcm(int a, int b) {
        return (a / gcd(a, b)) * b;
    }

    void solve() {
        int t; cin >> t;
        while (t--) {
            int n; cin >> n;
            set<int> st; 
            for (int i = 0; i < n; i++) {
                int a; cin >> a;
                st.insert(a);
            }

            cout << st.size() << "\n";
        }
    }

};


int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);

    Solution().solve();
    return (0);
}
